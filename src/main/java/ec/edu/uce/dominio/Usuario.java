package ec.edu.uce.dominio;

public class Usuario {
    private String id;
    private String nombre;
    private String apellido;
    private String correo;
    private String contraseña;

    public Usuario(String id, String nombre, String apellido, String correo, String contraseña) {
        this.id = id;
        this.nombre = nombre;
        this.apellido = apellido;
        this.correo = correo;
        this.contraseña = contraseña;
    }

    // Getters, Setters y toString...



// Getters y Setters
    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getApellido() {
        return apellido;
    }

    public void setApellido(String apellido) {
        this.apellido = apellido;
    }

    public String getCorreo() {
        return correo;
    }

    public void setCorreo(String correo) {
        this.correo = correo;
    }

    public String getContraseña() {
        return contraseña;
    }

    public void setContraseña(String contraseña) {
        this.contraseña = contraseña;
    }
    // Operaciones
    public boolean iniciarSesion(String correo, String contrasena) {
        return this.correo.equals(correo) && this.contraseña.equals(contrasena);
    }

    public void cerrarSesion() {
        System.out.println("Sesión cerrada para el usuario: " + nombre);
    }
    @Override
    public String toString() {
        return "Usuario{" +
                "id='" + id + '\'' +
                ", nombre='" + nombre + '\'' +
                ", apellido='" + apellido + '\'' +
                ", correo='" + correo + '\'' +
                '}';
    }
}
